<script setup lang="ts">
import SkeletonWrapper from "@/FormWrappers/SkeletonWrapper.vue";
import {computed} from "vue";

const props = defineProps({
  statLevelInfo: {
    type: Object,
    required: true,
  },
  isLoading: {
    type: Boolean,
    required: false,
    default: false
  },
  currentLevelXp: {
    type: Number,
    required: false,
    default: 0
  },
  currentLevelId: {
    type: Number,
    required: false,
    default: 0
  }
});

const showXPTag = computed(() => {
  return props.statLevelInfo.level !== props.currentLevelId
})

const plusOrMinusSign = computed(() => {
  return props.statLevelInfo.level > props.currentLevelId ? "-" : "+";
});

</script>

<template>
  <div class="row">
    <div class="col-4 text-center">
      <div class="mb-2">
        Level
      </div>
      <div>
        <SkeletonWrapper :show-skeleton="isLoading" height="2rem" width="100%">
          {{ props.statLevelInfo.level }}
        </SkeletonWrapper>
      </div>
    </div>
    <div class="col-4 text-center">
      <div class="mb-2">
        Bonus
      </div>
      <div>
        <SkeletonWrapper :show-skeleton="isLoading" height="2rem" width="100%">
          <span v-if="props.statLevelInfo.bonus > 0">+</span>{{ props.statLevelInfo.bonus }}
        </SkeletonWrapper>
      </div>
    </div>
    <div class="col-4 text-center">
      <div class="mb-2">
        XP
      </div>
      <div>
        <SkeletonWrapper :show-skeleton="isLoading" height="2rem" width="100%">
          {{ props.statLevelInfo.xp }}<span v-if="showXPTag"> ( {{ plusOrMinusSign }}{{ Math.abs(props.statLevelInfo.totalXP - props.currentLevelXp) }} )</span>
        </SkeletonWrapper>
      </div>
    </div>
  </div>
  <div class="row">
    <div class="col">
      <SkeletonWrapper :show-skeleton="isLoading" height="4em" width="100%">
        {{ props.statLevelInfo.description }}
      </SkeletonWrapper>
    </div>
  </div>
</template>
